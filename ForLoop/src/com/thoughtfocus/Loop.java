package com.thoughtfocus;

public class Loop {

	public void loop1() {
		// Code of Java for loop
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
	}

	public void loop2() {
		int arr[] = { 12, 23, 44, 56, 78 };
		// Printing array using for-each loop
		for (int i : arr) {
			System.out.println(i);
		}
	}
}
